                                                    <h3 class="eduopen-link coursedetailsh3">
                                <img class="pathlangflag img-responsive" src="http://dev.eduopen.org/theme/eduopen/pix/countryflag/svg/it.png">
                                Il corso                            </h3>
                            <p><span>the course provides an introduction to the historical circumstances surrounding the beginnings of welfare measures in Europe in the wake of the Industrial Revolution. It is based on the sociological categorisation of pre-modern and modern forms of solidarity, the latter of which have to be organised, fostered and protected by explicit welfare arrangements.</span></p>
                        
                                                    <h3 class="eduopen-link coursedetailsh3">
                                Learning outcomes                            </h3>
                            <p><span></span></p><p>The module provides participants with a basic understanding of the specific nature of social work as an academic discipline and as a profession from an historical perspective. It therefore helps to correct misunderstandings of the helping process in social services and allows to recognise the personal and the political nature of this highly delicate process. Students will learn that the processes that brought about modern societies, with their promises of liberty and equality but also with their dangers of dependency, destitution and exclusion, are continuously at work. They will be able to identify the outlines of similarities and differences&nbsp; between present social policy options and those of early industrialisation.</p><p></p>
                        
