                                    About the Course                                </h3>
                                <span><p>Sozial work is a typically modern profession and discipline. Ist development was necessitated by the economic, social and political transformation which marked the transition from a traditional to a modern society. Ist mandate is to support people, threatened with isolation, poverty, marginalisation as well as their psychological implications to realise their social citizenship. This development took place in various phases which are briefly introduced, in parallel with the various models of "welfare regimes" that came to characterise European nation states. The purpose of the module is to show that "helping" of this formal kind impliesthe mediation between "private troubles" and "public issues" and is therefore always of a political nature.</p></span>
